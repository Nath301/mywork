char	*ft_strstr(char *str, char *to_find)
{
	int	i;
	int	n;

	n = 0;
	if (to_find[0] == '\0')
		return (str);
	while (str[n] != '\0')
	{
		i = 0;
		while (to_find[i] != '\0' && str[n + i] == to_find[i])
		{
			if (to_find[i + 1] == '\0')
				return (&str[n]);
			i++;
		}
		n++;
	}
	return (0);
}
