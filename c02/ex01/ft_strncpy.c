char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	unsigned int	i;

	i = 0;
	while (i <= n - 1 && src[i] != 0)
	{
		dest[i] = src [i];
		i++;
	}
	while (i <= n - 1)
	{
		dest[i] = '0';
		i++;
	}
	return (dest);
}
